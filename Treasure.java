package com.example.deadfishapp;

public class Treasure {
    //data members
    private int mX;
    private int mY;

    //setters and getters for treasure chest
    public int getX() {
        return mX;
    }

    public void setX(int x) {
        mX = x;
    }

    public int getY() {
        return mY;
    }

    public void setY(int y) {
        mY = y;
    }
}
